package keywordsAndExpressions;

public class keywrdsAndExps {

	public static void main(String[] args) {
		int inti = 50; 
		/**
		 * inti = 50 is an expression and int is a keyword
		 * int inti = 50; is a statement
		 */
		
		if (inti < 100){
			System.out.println("It is less than 100");
			/**
			 * In the above print statement, it is less than 100
			 * is the expression
			 */
		}
		
		/**
		 * After this lecture, tutor discussed about importance of
		 * indentation in code blocks, how 'auto format' option in eclipse works
		 * and also about white spaces
		 */

	}

}
